第27条（知的財産権等）
１．サービス用設備等を構成するソフトウェア、Goalousに関する商標、コンテンツ、Goalous提供のためにユーザーに提示した資料等に関する全ての知的財産権（以下「本件知的財産権」といいます）については、ISAO及び本件委託先その他正当な権利を有する第三者（以下、総称して「権利者」といいます）に帰属するものとし、Goalous利用契約の締結によりユーザーに対して何らの権利を移転し、又は本規約に定める以外の使用又は利用を許諾するものではありません。  
２．ISAOは、ユーザーに対して、ユーザーによるGoalous利用において必要な範囲及び期間内で、非独占的な本件知的財産権の使用許諾権を与えるものとします。その場合、Goalous利用料に当該使用許諾の対価が含まれているものとします。  
