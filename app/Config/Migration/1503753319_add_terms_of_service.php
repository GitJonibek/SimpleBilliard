第28条（権利義務譲渡の禁止）
ユーザーは、第三者に対し、Goalous利用契約上の契約者たる地位又はこれに基づく権利若しくは義務の全部若しくは一部を第三者に対し譲渡又は担保提供してはならないものとします。  

第29条（通知方法）  
１．本規約に基づきISAOがユーザーに対して行なう通知その他の連絡は、Goalous上への掲示、ISAOホームページによる掲示、電子メール、書面等の方式のうち、ISAOが選択する適切かつ合理的な方式で行ないます。  
２．前項の通知その他の連絡先は、ユーザーの届け出たものとします。ユーザーの届け出た連絡先が事実とは異なるために通知その他の連絡がユーザーに到達しなかったときは、その通知等が通常到達すべきときにユーザーに到達したものとみなします。  
３．通知その他の連絡を電子メールにより行った場合は、ISAOがユーザーの届け出た連絡先のアドレスに電子メールを発信した時点で到達したものとみなし、Goalousおよびホームページへの掲載により行った場合は、ユーザーが当該ページを閲覧することが可能となった時点で到達したものとみなします。  

第30条（分離性）
本規約において、条項の全部または一部が法令上無効とされた場合、当該条項についてのみ効力を失うものとし、それ以外の各条項は引き続き有効なものとして、ユーザーおよびISAOに適用されるものとします。  

第31条（準拠法）
本規約の成立、効力、履行及び解釈に関する準拠法は、日本法とします。  

第32条（協議等）
本規約に規定のない事項及び規定された項目について疑義が生じた場合は両者誠意を持って協議の上解決することとします。  

第33条（合意管轄）
本規約の内容につき、ユーザーとISAOの間で紛争が生じ、訴訟又は調停の必要がある場合には、東京地方裁判所を第一審の専属的合意管轄裁判所とします。  

