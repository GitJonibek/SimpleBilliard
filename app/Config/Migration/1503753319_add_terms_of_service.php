２．チーム内情報発信の統制やチーム内情報発信に含まれるチームメンバーの個人情報の取扱方法等については、チーム内で解決するものとし、ISAOはこれに関与する義務を負わないものとします。  
３．チーム内情報発信にもとづいてチームメンバーが損害を被ったとしても、ISAOはその損害について賠償の責めを負わないものとします。  

第22条（秘密保持義務）
１．ユーザーは、Goalous利用契約に関連し、若しくは付随してISAOから提供され又は開示を受ける技術上、営業上、財務上並びにその他の一切の資料および情報（総称して、以下「本件機密情報」といいます）を秘密として厳重に管理および維持し、事前に相手方から書面による承諾を得ることなく本件機密情報のいかなる部分も第三者に一切開示し又は漏洩しないものとします。  
２．前項の規定にかかわらず、ユーザーにおいて、以下の各号の一に該当することを証明した情報についてはこの限りではない。
(1) 既に公知、公用の情報。  
(2) 開示後ユーザーの責によらず公知、公用となった情報。  
(3) 開示を受けた時に既に知得していた情報。  
(4) 開示を受けた後、正当な権限を有する第三者より守秘義務を負うことなく入手した情報。  
(5) 法令により開示することが義務づけられた情報。  
(6) 開示された本件機密情報と無関係に開発、創作した情報。  
３．ユーザーは、本件機密情報を、当該本件機密情報を知る必要のある自己の役員および従業員のみに開示することができるものとし、当該役員および従業員に対して本条に定める秘密保持義務を遵守させるものとします。  
４．ユーザーは、事前の書面による相手方の承諾を得ることなく、本件機密情報を本規約の履行以外の目的で一切使用してはならないものとします。  

第23条（データ等の取扱い）
１．ISAOは、データ等をGoalous利用契約の履行のためにのみ使用し、他の目的には使用しません。  
２．ISAOは、データ等を以下の各号に定める場合のほか第三者に開示しません。  
(1) 法令に基づく開示の義務がある場合  
(2) 生命、身体又は財産を保護するためにやむを得ない事由がある場合  
(3) Goalous利用契約を履行するために本件委託先に開示する場合  
(4) その他Goalous利用契約を履行する上でやむを得ない事由がある場合  

第24条（個人情報の取り扱い）
ISAOは、ユーザーから取得した個人情報（個人情報とは、生存する個人に関する情報であって、当該情報に含まれる氏名、生年月日その他の記述等により特定の個人を識別することができる情報〔他の情報と容易に照合することができ、それにより特定の個人を識別することができることとなるものを含む〕。以下「本件個人情報」といいます）については、個人情報保護法に従って適切に取り扱うものとします。  

